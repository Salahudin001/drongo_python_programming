

class LightSwitch():

    def __init__(self):
        self.switchIsOn = False

    def turnOn(self):
        # turn the switch on
        self.switchIsOn = True

    def turnOff(self):
        # turn the switch off
        self.switchIsOn = False

    def show(self): # added for testing
        print(self.switchIsOn)

# Main code
sittingroom = LightSwitch()
# Calls to methods to switch the lights on and off
sittingroom.show()
sittingroom.turnOn()
